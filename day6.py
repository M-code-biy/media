#IDENTITY OPERATOR
a=[1,2,3]
b=a
c=[1,2,3]
print(a is b)
print(a is c)
print(a is not c)

#Comparision operator
x=5
y=10
print(x==y)
print(x!=y)
print(x>y)
print(x<y)
print(x>=y)
print(x<=y)

#Membership operator
fruits=["apple","banana","cherry"]
print("apple" in fruits)
print("grape" not in fruits)

